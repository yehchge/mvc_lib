<p>
    This application is to demonstrate the use of the <b>JREAM MVC</b>.
    It should be easy to follow if you've ever used an MVC.
</p>

<p>
    This project leaves a lot of freedom up to you and how you'd like to configure
    things. It's left at a minimal way, and currently all the configuration needed
    is setup in the <b>index.php</b> file.
</p>

<p>
    To configure your database connection, go to <b>model/model.php</b>. That is the
    only place you should make your database calls!
</p>